<h3>1.3</h3>
<ul>
  <li>Firmware version 2.0.4 or greater is now required</li>
  <li>Tabs are now created with the drawTabs function for forward firmware compatibility</li>
  <li>UI has been re-designed to look much nicer</li>
  <li>Removed javascript in notifications</li>
  <li>File Tracker refreshes independently from the rest of the tile now</li>
</ul>

<h3>1.2</h3>
<ul>
  <li>Large tile now refreshes when file is encrypted/decrypted</li>
  <li>Firmware version 1.4 or greater is now required.</li>
</ul>

